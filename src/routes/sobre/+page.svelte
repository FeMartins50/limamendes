    <h1>67ª competição Troféu Tenente Lima Mendes</h1>
    <p>Durante os dias 21 a 27 de março, ocorre, na EPCAR (Escola Preparatória de Cadetes do Ar), a 67ª competição Troféu Tenente Lima Mendes. Os três esquadrões da escola, Cavok, Thunderbolt e Xavante, disputam entre si onze modalidades esportivas, sendo esgrima, vôlei, futebol, natação, basquete, judô, orientação, tiro, triatlo militar, vôlei e xadrez. Além disso, o evento conta com a modalidade de participação, que compreende biatlo, cabo de guerra e corrida rústica.</p>    
    <p>Os jogos ocorrem anualmente e são abertos ao público, contando com a participação de familiares dos alunos e demais visitantes, que animam ainda mais a torcida. O objetivo da competição é fortalecer a camaradagem e companheirismo, incentivar o espírito de corpo entre as turmas, além de despertar o interesse e a paixão pelos esportes.</p>
</section>

<section>
    <h1>Tenente Lima Mendes</h1>
    <p>O Tenente Aviador Lima Mendes foi um Piloto de Combate brasileiro, conhecido por seu importante papel durante a Segunda Guerra Mundial. Ele comandou a Força Aérea Brasileira (FAB) na Itália, onde se destacou em missões de combate, incluindo a escolta de bombardeiros aliados. Sua bravura e habilidade tática contribuíram significativamente para o esforço de guerra do país e, mesmo após o fim dos conflitos, Lima Mendes continuou seu trabalho, como Instrutor de voo na Base Aérea de Santa Cruz. Como forma de honrar seu legado e dedicação, instituiu-se na EPCAR, em 21 de maio de 1951, o Troféu Tenente Lima Mendes como nome à disputa anual entre os esquadrões, que já ocorria desde 1949.</p>
</section>

